#!/usr/bin/env bash
# Evidence emitter for the self-hosted verification loop (ggen-verify-pack).
#
# Runs the real check matrix ONCE and records the results twice:
#   1. evidence/checks.ttl            -- ver: facts the sync gates enforce
#   2. receipts/inspection-receipt.json -- array conforming to the reference's
#      own schemas/build-receipt.schema.json (schema, step, recorded_at,
#      source_digest, status; additionalProperties permitted)
#
# This script ALWAYS exits 0: it records reality, it does not judge it.
# Judgment belongs to packs/ggen-verify-pack/gates/*.rq at the next
# `ggen sync run` -- red or missing evidence there is an FM-PACK-013
# refusal. That split (emit facts / gate facts) is the whole point.
set -uo pipefail

cd "$(dirname "$0")/.."
ROOT="$(pwd)"
REF_CORE="$ROOT/../../packs/tcps-core-pack/reference/製品版/crates/tcps-core/src"
mkdir -p evidence receipts

RECORDED_AT="$(date -u +%Y-%m-%dT%H:%M:%SZ)"

# --- graph-hash binding (freshness gate) -----------------------------------
# Bind this evidence to the latest completed sync's graph hash. On a
# never-synced tree there is no receipt yet; record "unsynced" (the
# freshness gate only fires when a ggenr:Sync fact exists to disagree with).
GRAPH_HASH="unsynced"
if [ -f .ggen-v2/receipt.json ]; then
  GRAPH_HASH="$(python3 -c 'import json;print(json.load(open(".ggen-v2/receipt.json"))["payload"]["graph_hash"])' 2>/dev/null || echo unsynced)"
fi

# --- source digest (64-hex, required by build-receipt.schema.json) ---------
SOURCE_DIGEST="$(find src crates -type f -name '*.rs' 2>/dev/null | LC_ALL=C sort | xargs shasum -a 256 2>/dev/null | shasum -a 256 | cut -d' ' -f1)"

# --- run the matrix, capturing REAL exit codes ------------------------------
run_check() { # name, command...
  local name="$1"; shift
  local log="/tmp/verify-$name-$$.log"
  "$@" >"$log" 2>&1
  local code=$?
  CHECK_NAMES+=("$name")
  CHECK_CODES+=("$code")
  CHECK_TOOLS+=("$*")
  CHECK_SUMMARIES+=("$(tail -1 "$log" | tr '"' "'" | cut -c1-200)")
  rm -f "$log"
}
CHECK_NAMES=(); CHECK_CODES=(); CHECK_TOOLS=(); CHECK_SUMMARIES=()

run_check build            cargo build --workspace
run_check test-workspace   cargo test --workspace
run_check clippy-reference-gate cargo clippy --workspace --all-targets -- -D clippy::correctness -D clippy::suspicious -D clippy::perf
run_check clippy-blanket-warnings cargo clippy --workspace --all-targets -- -D warnings
run_check fmt-check        cargo fmt --check

# --- byte-identity sweep vs the reference ------------------------------------
# Design-divergent allowlist (documented in tcps_conformance_e2e.rs and the
# maturity model): these stay divergent by design (_impl renames + added
# consts + product test file). Everything else in src/*.rs must be
# byte-identical to the reference module of the same name.
ALLOW="lib.rs 自動選択.rs 青い川のダム.rs 暗号要約.rs 改善.rs 試験.rs ggen_pack_mods.rs"
BI_DIVERGENT=""
BI_IDENTICAL=0
for f in src/*.rs; do
  base="$(basename "$f")"
  case " $ALLOW " in *" $base "*) continue ;; esac
  if [ -f "$REF_CORE/$base" ]; then
    a="$(shasum -a 256 "$f" | cut -d' ' -f1)"
    b="$(shasum -a 256 "$REF_CORE/$base" | cut -d' ' -f1)"
    if [ "$a" = "$b" ]; then BI_IDENTICAL=$((BI_IDENTICAL+1)); else BI_DIVERGENT="$BI_DIVERGENT $base"; fi
  fi
done
if [ -z "$BI_DIVERGENT" ]; then BI_CODE=0; BI_SUM="$BI_IDENTICAL modules byte-identical to reference; allowlist: $ALLOW"; else BI_CODE=1; BI_SUM="non-allowlisted divergence:$BI_DIVERGENT ($BI_IDENTICAL identical)"; fi
CHECK_NAMES+=("byte-identity"); CHECK_CODES+=("$BI_CODE"); CHECK_TOOLS+=("sha256 sweep src/*.rs vs reference tcps-core src"); CHECK_SUMMARIES+=("$BI_SUM")

# --- emit evidence/ontology.ttl (the evidence mini-pack's union-graph input) --
# evidence/ is wired in the consumer's [packs] as a local pack (pack.toml is
# static, this ontology is regenerated every run) -- evidence enters the graph
# through the same pack-admission path as every other governing input.
{
  echo "# GENERATED by scripts/verify.sh at $RECORDED_AT -- do not edit."
  echo "# Real exit codes from real command runs; gated by ggen-verify-pack."
  echo "@prefix ver: <http://seanchatmangpt.github.io/packs/ggen-verify#> ."
  echo
  for i in "${!CHECK_NAMES[@]}"; do
    n="${CHECK_NAMES[$i]}"
    echo "ver:check-$n a ver:Check ;"
    echo "    ver:name \"$n\" ;"
    echo "    ver:tool \"${CHECK_TOOLS[$i]}\" ;"
    echo "    ver:exitCode ${CHECK_CODES[$i]} ;"
    echo "    ver:summary \"${CHECK_SUMMARIES[$i]}\" ;"
    echo "    ver:verifiedGraphHash \"$GRAPH_HASH\" ;"
    echo "    ver:recordedAt \"$RECORDED_AT\" ."
    echo
  done
} > evidence/ontology.ttl

# --- emit receipts/inspection-receipt.json (schema-conformant array) ---------
{
  echo "["
  for i in "${!CHECK_NAMES[@]}"; do
    code="${CHECK_CODES[$i]}"
    status="pass"; [ "$code" -ne 0 ] && status="fail"
    comma=","; [ "$i" -eq $((${#CHECK_NAMES[@]}-1)) ] && comma=""
    printf '  {"schema": "build-receipt/v1", "step": "%s", "recorded_at": "%s", "source_digest": "%s", "status": "%s", "exit_code": %s, "tool": "%s", "summary": "%s"}%s\n' \
      "${CHECK_NAMES[$i]}" "$RECORDED_AT" "$SOURCE_DIGEST" "$status" "$code" "${CHECK_TOOLS[$i]}" "${CHECK_SUMMARIES[$i]}" "$comma"
  done
  echo "]"
} > receipts/inspection-receipt.json

echo "verify.sh: recorded ${#CHECK_NAMES[@]} checks -> evidence/ontology.ttl + receipts/inspection-receipt.json (graph hash binding: $GRAPH_HASH)"
for i in "${!CHECK_NAMES[@]}"; do
  echo "  ${CHECK_NAMES[$i]}: exit ${CHECK_CODES[$i]}"
done
exit 0
