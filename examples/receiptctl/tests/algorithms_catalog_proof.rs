//! Proof tests for `wasm4pm-algorithms-pack`'s generated catalog
//! (`src/w4pm_algorithms_catalog.rs`).
//!
//! Unlike the pre-2026-07-18 version of this file (which asserted only 8
//! hand-transcribed spot-check rows and 2 hand-counted magic numbers, 60 and
//! the 9 per-category counts), every literal in the two blocks below --
//! `EXPECTED_TOTAL`, `EXPECTED_CATEGORY_COUNTS`, and the full per-row
//! assertions in `generated_full_field_assertions_over_every_catalog_row` --
//! is rendered from a live SPARQL query over `ontology.ttl` at `ggen sync`
//! time via this same template's `algorithms`/`category_counts` bindings, not
//! copied by hand. Adding, removing, or editing any
//! `pi:ProcessIntelligenceAlgorithm` individual changes these assertions on
//! the next sync automatically -- closing the exact "Test generation" /
//! "Regeneration lifecycle" gap the L5 validation report named for this
//! pack ("full-field assertions ... across all 60 rows and all 11 ontology
//! properties ... or an independently-derived second query path replacing
//! the hand-transcribed literals").
//!
//! `AlgorithmInfo` only carries 6 runtime fields (id, algorithm_id, label,
//! category, wasm_export, cli_alias) -- speed_tier/quality_tier/doc/citation
//! exist only as Rust doc-comments on the `AlgorithmId` enum or in the
//! separate `docs/w4pm_algorithms.md`, so this file does not (and cannot)
//! assert on them at runtime.
//!
//! Generated by `wasm4pm-algorithms-pack`.

#[path = "../src/w4pm_algorithms_catalog.rs"]
mod w4pm_algorithms_catalog;

use std::collections::HashSet;
use w4pm_algorithms_catalog::{by_wasm_export, AlgorithmId, AlgorithmInfo, CATALOG};

/// Linear lookup by `algorithm_id`. A plain search over the generated table,
/// not a re-derivation of any expected field value.
fn find(algorithm_id: &str) -> &'static AlgorithmInfo {
    CATALOG
        .iter()
        .find(|row| row.algorithm_id == algorithm_id)
        .unwrap_or_else(|| panic!("no CATALOG row for algorithm_id={algorithm_id:?}"))
}

// ---------------------------------------------------------------------------
// Cardinality -- SPARQL-derived (sparql_count over the same `algorithms`
// binding used by src/w4pm_algorithms_catalog.rs.tmpl), not hand-counted.
// ---------------------------------------------------------------------------

const EXPECTED_TOTAL: usize = 60;

#[test]
fn catalog_len_matches_sparql_derived_total() {
    assert_eq!(CATALOG.len(), EXPECTED_TOTAL);
}

/// (category, expected_count) pairs derived from a `GROUP BY ?category`
/// aggregate query, not hand-counted from ontology.ttl's section headers.
const EXPECTED_CATEGORY_COUNTS: &[(&str, usize)] = &[
    ("agentic", 1),
    ("conformance", 4),
    ("discovery", 20),
    ("discovery_analytics", 10),
    ("import_export", 4),
    ("ml_analytics", 8),
    ("object_centric", 6),
    ("prediction", 5),
    ("simulation", 2),
];

#[test]
fn category_counts_match_sparql_group_by_aggregate() {
    fn count(category: &str) -> usize {
        CATALOG.iter().filter(|row| row.category == category).count()
    }
    for (category, expected) in EXPECTED_CATEGORY_COUNTS {
        assert_eq!(
            count(category),
            *expected,
            "category {:?} count mismatch vs SPARQL GROUP BY aggregate",
            category
        );
    }
    // The sum of the per-category SPARQL aggregate must equal the total --
    // catches a category ever silently dropped from the GROUP BY grouping.
    let sum: usize = EXPECTED_CATEGORY_COUNTS.iter().map(|(_, n)| *n).sum();
    assert_eq!(sum, EXPECTED_TOTAL);
}

// ---------------------------------------------------------------------------
// Structural invariants -- uniqueness `by_wasm_export`'s correctness depends
// on. These check a property of the whole table, not any single row's value.
// ---------------------------------------------------------------------------

#[test]
fn wasm_export_values_are_unique_across_the_catalog() {
    let mut seen: HashSet<&str> = HashSet::new();
    for row in CATALOG {
        assert!(
            seen.insert(row.wasm_export),
            "duplicate wasm_export {:?} would make by_wasm_export ambiguous",
            row.wasm_export
        );
    }
    assert_eq!(seen.len(), EXPECTED_TOTAL);
}

#[test]
fn algorithm_id_values_are_unique_across_the_catalog() {
    let mut seen: HashSet<&str> = HashSet::new();
    for row in CATALOG {
        assert!(
            seen.insert(row.algorithm_id),
            "duplicate algorithm_id {:?}",
            row.algorithm_id
        );
    }
    assert_eq!(seen.len(), EXPECTED_TOTAL);
}

