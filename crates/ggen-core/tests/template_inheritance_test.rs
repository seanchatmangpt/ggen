//! Tests for Tera template inheritance ({% extends %}, {% include %}, {% block %})
//!
//! These tests verify that:
//! 1. The base `_head.tera` template parses without error
//! 2. Child templates correctly extend `_head.tera` via `{% extends %}`
//! 3. Block overrides produce valid merged output (header + imports + body)
//! 4. Missing base template causes a Tera error (not a panic)
//! 5. All child templates in `mcp-server/` parse and extend correctly
//! 6. `{% include %}` templates resolve correctly (benchmark components)
//! 7. No circular `{% extends %}` chains exist across the template tree
//!
//! NOTE: Tera validates `{% extends %}` at `add_raw_template` time, so the parent
//! template must be registered before the child. Similarly, `{% include %}` targets
//! must be registered before the template that includes them.

use std::collections::HashMap;
use tera::{Context, Tera};
use walkdir::WalkDir;

fn workspace_root() -> std::path::PathBuf {
    let mut p = std::path::PathBuf::from(env!("CARGO_MANIFEST_DIR"));
    p.pop(); // crates/ggen-core -> crates
    p.pop(); // crates -> ggen root
    p
}

/// Read a template file relative to the ggen workspace root, returning None if missing.
fn read_template(rel: &str) -> Option<String> {
    let path = workspace_root().join(rel);
    if !path.exists() {
        return None;
    }
    Some(
        std::fs::read_to_string(&path)
            .unwrap_or_else(|e| panic!("Cannot read {}: {}", path.display(), e)),
    )
}

/// Create a Tera instance with all ggen-core filters/functions registered.
fn create_tera() -> Tera {
    let mut tera = Tera::default();
    ggen_core::register::register_all(&mut tera);
    tera
}

