    let child = read_template("crates/ggen-core/templates/mcp-server/stdio_server.rs.tera");
    if child.is_none() {
        return;
    }

    // Load child WITHOUT the base template. Tera validates extends at
    // add_raw_template time, so this should return an Err, not panic.
    let mut tera = create_tera();
    let result = tera.add_raw_template("mcp-server/stdio_server.rs.tera", &child.unwrap());

