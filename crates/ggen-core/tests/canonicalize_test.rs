#[test]
fn test_canonicalize_preserves_existing_header() {
    let input = "// DO NOT EDIT this file\nfn main() {}\n";
    let options = CanonicalizeOptions::new().with_inject_header(true);
    let result = canonicalize(input, &options).unwrap();
    // Should not duplicate the header
    let count = result.matches("DO NOT EDIT").count();
    assert_eq!(count, 1);
}

#[test]
fn test_canonicalize_import_grouping() {
    let input = r#"
use crate::module::Struct;
use std::collections::HashMap;
use external_crate::Trait;
use std::vec::Vec;
use crate::module::Type;
"#;

    let options = CanonicalizeOptions::new().with_sort_imports(true);
    let result = canonicalize(input, &options).unwrap();

    // Check that imports are grouped: std → external → internal
    let lines: Vec<&str> = result.lines().collect();
    let mut std_idx = None;
    let mut external_idx = None;
    let mut internal_idx = None;

    for (i, line) in lines.iter().enumerate() {
        if line.contains("use std::collections") {
            std_idx = Some(i);
        }
        if line.contains("use std::vec") {
            // vec should be after collections
            assert!(std_idx.unwrap_or(0) < i);
        }
        if line.contains("use external_crate") {
            external_idx = Some(i);
        }
        if line.contains("use crate::module") && internal_idx.is_none() {
            internal_idx = Some(i);
        }
    }

    // Verify ordering: std < external < internal
    if let (Some(std), Some(ext), Some(int)) = (std_idx, external_idx, internal_idx) {
        assert!(std < ext, "std imports should come before external");
        assert!(ext < int, "external imports should come before internal");
    }
}

#[test]
fn test_canonicalize_a2a_standard() {
    let input = r#"use std::collections::HashMap;
use std::vec::Vec;

fn main() {}
"#;

    let result = canonicalize_a2a(input).unwrap();

    // Should have header
    assert!(result.contains("DO NOT EDIT"));
    assert!(result.contains("Generated by ggen"));

    // Should maintain proper ordering
    let hash_pos = result.find("std::collections::HashMap").unwrap();
    let vec_pos = result.find("std::vec::Vec").unwrap();
    assert!(hash_pos < vec_pos);
}

