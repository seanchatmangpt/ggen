#[test]
fn test_canonicalize_preserves_shebang() {
    let input = "#!/usr/bin/env rust-script\nfn main() {}\n";
    let options = CanonicalizeOptions::new().with_inject_header(true);
    let result = canonicalize(input, &options).unwrap();

    // Shebang should be first
    assert!(result.starts_with("#!/usr/bin/env rust-script"));

    // Header should come after shebang
    assert!(result.contains("DO NOT EDIT"));

    // Content should be preserved
    assert!(result.contains("fn main()"));
}

#[test]
fn test_import_complex_paths() {
    let input = r#"
use std::sync::Arc;
use std::collections::HashMap;
use crate::module::inner::Struct;
use crate::module::Type;
"#;

    let options = CanonicalizeOptions::new().with_sort_imports(true);
    let result = canonicalize(input, &options).unwrap();

    // std imports should come before crate imports
    let std_pos = result.find("use std::").unwrap();
    let crate_pos = result.find("use crate::").unwrap();
    assert!(std_pos < crate_pos);
}

