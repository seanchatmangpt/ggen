#[test]
fn test_canonicalize_file_by_extension() {
    let rs_content = "use std::b; use std::a;";
    let json_content = r#"{"b":1,"a":2}"#;
    let toml_content = "[package]\r\nname=\"test\"";

    let options = CanonicalizeOptions::new()
        .with_sort_imports(true)
        .with_normalize_line_endings(true);

    // Test .rs file
    let rs_result = canonicalize_file(Path::new("test.rs"), rs_content, &options).unwrap();
    assert!(rs_result.contains("use std::a"));

    // Test .json file
    let json_result = canonicalize_file(Path::new("test.json"), json_content, &options).unwrap();
    assert!(json_result.contains("\n")); // Pretty printed

    // Test .toml file
    let toml_result = canonicalize_file(Path::new("test.toml"), toml_content, &options).unwrap();
    assert!(!toml_result.contains("\r\n"));
}

