//! Hierarchical (2-level) POWL projection of Joseph's famine-management
//! plan: the same 26-artifact corpus as `chatman_pddl_to_powl_joseph_famine`
//! (kept untouched as the flat regression baseline), but projected via
//! `project_pddl_tape_to_powl_hierarchical` so the ONE combined national
//! plan is grouped into phases by contributing source artifact — a phase
//! being a maximal run of tape-adjacent plan ops whose action schemas were
//! contributed by the same domain fragment. Root is a PartialOrder of
//! per-phase PartialOrders; institutional structure (which artifact
//! contributed which steps) becomes visible workflow nesting, with no new
//! semantic authority beyond the supplied provenance mapping.
//!
//! Artifact boundary: identical to the flat test — Turtle/PDDL is imported
//! from committed `.ttl` files (`tests/pddl_to_powl/joseph/`) and exported
//! to one generated `.ttl` file; no Turtle or PDDL is embedded in Rust
//! strings; the POWL output is generated by the pipeline, never
//! hand-authored.

