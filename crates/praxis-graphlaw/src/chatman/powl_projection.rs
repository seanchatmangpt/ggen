pub fn powl_to_turtle(
    model: &Powl,
    base_iri: &str,
    derived_from: Option<&str>,
) -> Result<String, Refusal> {
    annotated_powl_to_turtle(model, base_iri, derived_from, &PowlAnnotations::default())
}

pub fn annotated_powl_to_turtle(
    model: &Powl,
    base_iri: &str,
    derived_from: Option<&str>,
    annotations: &PowlAnnotations,
) -> Result<String, Refusal> {
    admit_powl_model(model)?;
    admit_provenance(model, base_iri, derived_from)?;

    let base_iri = base_iri.trim_end_matches('/');
    let mut out = String::new();
    out.push_str("@prefix powl2: <");
    out.push_str(POWL2_PREFIX);
    out.push_str("> .\n");
    out.push_str("@prefix base: <");
    out.push_str(base_iri);
    out.push_str("/> .\n\n");

    let root_path = "n0";
    out.push_str(&format!("<{base_iri}/{root_path}> a powl2:Model .\n"));
    if let Some(source_iri) = derived_from {
        out.push_str(&format!(
            "<{base_iri}/{root_path}> powl2:derivedFrom <{source_iri}> .\n"
        ));
    }
    emit_powl_node(model, base_iri, root_path, &SocketPath::root(), annotations, &mut out);
    Ok(out)
}

/// Recursively emits Turtle triples for `model`, whose subject IRI is
/// `<base_iri>/<path>`. Child IRIs are `<base_iri>/<path>/c<index>` and
/// `PartialOrder` child-bindings are addressed at
/// `<base_iri>/<path>/binding/<index>` — both deterministic, structural, and
/// collision-free (each level appends a distinct literal segment).
///
/// # Complexity
/// O(n) in the size of the subtree rooted at `model`.
fn emit_powl_node(model: &Powl, base_iri: &str, path: &str, socket_path: &SocketPath, annotations: &PowlAnnotations, out: &mut String) {
    if let Some(inv) = annotations.process_invariants.get(socket_path) {
        out.push_str(&format!("<{base_iri}/{path}> powl2:hasInvariant \"{}\" .\n", escape_turtle_literal(inv)));
    }
    if let Some(evt) = annotations.exogenous_events.get(socket_path) {
        out.push_str(&format!("<{base_iri}/{path}> powl2:hasExogenousEvent \"{}\" .\n", escape_turtle_literal(evt)));
    }
    if let Some(res) = annotations.resource_annotations.get(socket_path) {
        out.push_str(&format!("<{base_iri}/{path}> powl2:hasResourceAnnotation \"{}\" .\n", escape_turtle_literal(res)));
    }

    match model {
        Powl::Leaf(None) => {
            out.push_str(&format!(
                "<{base_iri}/{path}> a powl2:Leaf, powl2:SilentLeaf .\n"
            ));
        }
        Powl::Leaf(Some(label)) => {
            out.push_str(&format!(
                "<{base_iri}/{path}> a powl2:Leaf, powl2:ActivityLeaf ;\n"
            ));
            out.push_str(&format!(
                "  powl2:activityLabel \"{}\" .\n",
                escape_turtle_literal(label)
            ));
        }
        Powl::PartialOrder { children, order } => {
            out.push_str(&format!("<{base_iri}/{path}> a powl2:PartialOrder .\n"));
            for (idx, child) in children.iter().enumerate() {
                let child_path = format!("{path}/c{idx}");
                let binding_path = format!("{path}/binding/{idx}");
                out.push_str(&format!(
                    "<{base_iri}/{path}> powl2:hasChild <{base_iri}/{binding_path}> .\n"
                ));
                out.push_str(&format!(
                    "<{base_iri}/{binding_path}> a powl2:ChildBinding ;\n  powl2:childIndex {idx} ;\n  powl2:childModel <{base_iri}/{child_path}> .\n"
                ));
                emit_powl_node(child, base_iri, &child_path, &socket_path.child(idx), annotations, out);
            }
            for (i, j) in order.iter() {
                out.push_str(&format!(
                    "<{base_iri}/{path}/binding/{i}> powl2:precedes <{base_iri}/{path}/binding/{j}> .\n"
                ));
            }
        }
        Powl::Choice { children, graph } => {
            // Vocabulary terms per powlv2.ttl: the ▷/□ sentinels are typed
            // powl2:StartNode / powl2:EndNode resources pointed at by the
            // functional powl2:startNode / powl2:endNode properties; each
            // routing edge is a reified powl2:GraphEdge with edgeSource /
            // edgeTarget; children are addressed both as ChildBindings and as
            // powl2:ChildNode routing-graph nodes.
            out.push_str(&format!("<{base_iri}/{path}> a powl2:ChoiceGraph .\n"));
            out.push_str(&format!(
                "<{base_iri}/{path}/start> a powl2:StartNode .\n<{base_iri}/{path}/end> a powl2:EndNode .\n"
            ));
            out.push_str(&format!(
                "<{base_iri}/{path}> powl2:startNode <{base_iri}/{path}/start> ;\n  powl2:endNode <{base_iri}/{path}/end> ;\n  powl2:hasNode <{base_iri}/{path}/start>, <{base_iri}/{path}/end> .\n"
            ));
            for (idx, child) in children.iter().enumerate() {
                let child_path = format!("{path}/c{idx}");
                let binding_path = format!("{path}/binding/{idx}");
                out.push_str(&format!(
                    "<{base_iri}/{path}> powl2:hasChild <{base_iri}/{binding_path}> .\n"
                ));
                out.push_str(&format!(
                    "<{base_iri}/{binding_path}> a powl2:ChildBinding ;\n  powl2:childIndex {idx} ;\n  powl2:childModel <{base_iri}/{child_path}> .\n"
                ));
                out.push_str(&format!(
                    "<{base_iri}/{path}/node/{idx}> a powl2:ChildNode ;\n  powl2:ofChildIndex {idx} .\n"
                ));
                out.push_str(&format!(
                    "<{base_iri}/{path}> powl2:hasNode <{base_iri}/{path}/node/{idx}> .\n"
                ));
                if let Some(guard) = annotations.guards.get(&(socket_path.clone(), idx)) {
                    out.push_str(&format!("<{base_iri}/{path}/node/{idx}> powl2:hasGuard \"{}\" .\n", escape_turtle_literal(guard)));
                }
                emit_powl_node(child, base_iri, &child_path, &socket_path.child(idx), annotations, out);
            }
            for (edge_idx, (from, to)) in graph.edges.iter().enumerate() {
                let from_iri = gnode_iri(base_iri, path, *from);
                let to_iri = gnode_iri(base_iri, path, *to);
                out.push_str(&format!(
                    "<{base_iri}/{path}/edge/{edge_idx}> a powl2:GraphEdge ;\n  powl2:edgeSource {from_iri} ;\n  powl2:edgeTarget {to_iri} .\n"
                ));
                out.push_str(&format!(
                    "<{base_iri}/{path}> powl2:hasEdge <{base_iri}/{path}/edge/{edge_idx}> .\n"
                ));
            }
        }
        Powl::ExternalCut {
            region,
            projection,
            renderer,
        } => {
            out.push_str(&format!("<{base_iri}/{path}> a powl2:ExternalCut .\n"));
            out.push_str(&format!(
                "<{base_iri}/{path}> powl2:hasRegion <{base_iri}/{path}/region> ;\n"
            ));
            out.push_str(&format!(
                "  powl2:sparqlProjection \"{}\" ;\n",
                escape_turtle_literal(projection)
            ));
            out.push_str(&format!(
                "  powl2:teraRenderer \"{}\" .\n",
                escape_turtle_literal(renderer)
            ));
            emit_powl_node(region, base_iri, &format!("{path}/region"), &socket_path.child(0), annotations, out);
        }
    }
}

/// Renders a [`GNode`] as a complete bracketed Turtle IRI term relative to the
/// choice-graph node at `<base_iri>/<path>`: the artificial `▷`/`□` sentinels
/// become fixed `/start` / `/end` suffixed IRIs, and a child index becomes its
/// `/node/<idx>` routing-graph node IRI.
///
/// # Complexity
/// O(1).
fn gnode_iri(base_iri: &str, path: &str, node: GNode) -> String {
    match node {
        GNode::Start => format!("<{base_iri}/{path}/start>"),
        GNode::End => format!("<{base_iri}/{path}/end>"),
        GNode::Child(idx) => format!("<{base_iri}/{path}/node/{idx}>"),
    }
}

