/// One flattened row of the Q(W) relational projection: the bound values of
/// one `?elementId` solution of [`RENDER_MODEL_PROJECTION_QUERY`]. Every
/// field but `element_id`/`element_type` is optional because the query's
/// `OPTIONAL` blocks bind only for the POWL element kinds that carry that
/// property -- e.g. `region_id`/`sparql_projection`/`tera_renderer` are
/// populated only for rows whose element is a `powl2:ExternalCut` subject.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct ProjectionRow {
    /// The projected element's subject IRI.
    pub element_id: String,
    /// One `rdf:type` object bound for this element (an element with
    /// multiple `rdf:type`s yields one row per type, per SPARQL BGP
    /// semantics for `?elementId a ?elementType`).
    pub element_type: String,
    /// `powl2:activityLabel`, bound when the element is a
    /// `powl2:ActivityLeaf`.
    pub activity_label: Option<String>,
    /// `powl2:childIndex` of a bound `powl2:hasChild` -> `ChildBinding` edge.
    pub child_index: Option<String>,
    /// `powl2:childModel` IRI of a bound `powl2:hasChild` -> `ChildBinding`
    /// edge.
    pub child_model: Option<String>,
    /// The target of a `powl2:precedes` edge from this element.
    pub precedes_target: Option<String>,
    /// `powl2:hasRegion` target, bound when the element is a
    /// `powl2:ExternalCut`.
    pub region_id: Option<String>,
    /// `powl2:sparqlProjection` literal (Q), bound when the element is a
    /// `powl2:ExternalCut`.
    pub sparql_projection: Option<String>,
    /// `powl2:teraRenderer` literal (T), bound when the element is a
    /// `powl2:ExternalCut`.
    pub tera_renderer: Option<String>,
    /// `powl2:derivedFrom` IRI, bound on the model root when
    /// [`powl_to_turtle`] was called with `derived_from = Some(_)`.
    pub derived_from: Option<String>,
    /// `powl2:startNode` target, bound when the element is a
    /// `powl2:ChoiceGraph` (Def 3.6's `▷` sentinel).
    pub start_node: Option<String>,
    /// `powl2:endNode` target, bound when the element is a
    /// `powl2:ChoiceGraph` (Def 3.6's `□` sentinel).
    pub end_node: Option<String>,
    /// One `powl2:hasNode` target of a bound `powl2:ChoiceGraph` element --
    /// a `▷`/`□` sentinel or a `powl2:ChildNode` routing-graph node.
    pub routing_node: Option<String>,
    /// `powl2:ofChildIndex` of `routing_node`, bound only when `routing_node`
    /// is a `powl2:ChildNode` (the `▷`/`□` sentinels carry none). Distinct
    /// from `child_index`, which is a `powl2:ChildBinding`'s index rather
    /// than a routing-graph node's.
    pub routing_node_child_index: Option<String>,
    /// One `powl2:hasEdge` target of a bound `powl2:ChoiceGraph` element --
    /// a reified `powl2:GraphEdge` subject IRI.
    pub edge_id: Option<String>,
    /// `powl2:edgeSource` of `edge_id`.
    pub edge_source: Option<String>,
    /// `powl2:edgeTarget` of `edge_id`.
    pub edge_target: Option<String>,
    /// `powl2:hasInvariant` of the element.
    pub process_invariant: Option<String>,
    /// `powl2:hasExogenousEvent` of the element.
    pub exogenous_event: Option<String>,
    /// `powl2:hasResourceAnnotation` of the element.
    pub resource_annotation: Option<String>,
    /// `powl2:hasGuard` of the element.
    pub guard_condition: Option<String>,
}

/// Executes the real Q-stage SPARQL projection ([`RENDER_MODEL_PROJECTION_QUERY`])
/// against `turtle` (as produced by [`powl_to_turtle`] for an admitted POWL
/// region) via a fresh in-memory oxigraph store, returning the flattened
/// relational rows Q(W) produces.
///
/// This is the SPARQL execution stage of PRD.md sec.7.4's `A_z = T(Q(W))`,
/// distinct from [`powl_to_turtle`] (which only produces the admitted
/// region's Turtle text, `W`): this function computes `Q` applied to `W`.
/// Deterministic: the query's own `ORDER BY` over every bound variable fixes
/// solution order, and each row's fields are read directly off the oxigraph
/// binding -- no HashMap, no wall clock, no randomness.
///
/// # Errors
/// [`Refusal::ValidationFailed`] if `turtle` fails to parse as Turtle, if
/// the embedded query fails to parse (a defect in this crate, not caller
/// input -- the query is a fixed compile-time constant), or if a solution
/// binds a projected variable to a term kind this projection does not
/// expect (the query only ever binds `?elementId`/`?elementType`/etc. to
/// IRIs or literals under [`powl_to_turtle`]'s IRI-only emission, so a
/// blank-node binding would indicate the input Turtle did not actually come
/// from that emitter).
///
/// # Complexity
/// O(t) to parse and load `turtle` (t = byte length) plus O(m log m) for the
/// query engine's `ORDER BY` over m solutions.
pub fn run_render_model_projection(turtle: &str) -> Result<Vec<ProjectionRow>, Refusal> {
    let store = Store::new().map_err(|e| {
        Refusal::ValidationFailed(format!("projection store construction failed: {e}"))
    })?;
    store
        .load_from_slice(RdfParser::from_format(RdfFormat::Turtle), turtle)
        .map_err(|e| Refusal::ValidationFailed(format!("projection Turtle load failed: {e}")))?;

    let prepared = SparqlEvaluator::new()
        .parse_query(RENDER_MODEL_PROJECTION_QUERY)
        .map_err(|e| {
            Refusal::ValidationFailed(format!(
                "render_model_projection.rq parse failed (crate defect): {e}"
            ))
        })?;
    let results = prepared.on_store(&store).execute().map_err(|e| {
        Refusal::ValidationFailed(format!("render_model_projection.rq execution failed: {e}"))
    })?;

    let mut rows = Vec::new();
    match results {
        QueryResults::Solutions(solutions) => {
            // O(m): one row per solution, in the query's own ORDER BY order.
            for solution in solutions {
                let solution = solution.map_err(|e| {
                    Refusal::ValidationFailed(format!("projection solution evaluation failed: {e}"))
                })?;
                let element_id = term_string(&solution, "elementId")?.ok_or_else(|| {
                    Refusal::ValidationFailed(
                        "projection row missing mandatory ?elementId".to_string(),
                    )
                })?;
                let element_type = term_string(&solution, "elementType")?.ok_or_else(|| {
                    Refusal::ValidationFailed(
                        "projection row missing mandatory ?elementType".to_string(),
                    )
                })?;
                rows.push(ProjectionRow {
                    element_id,
                    element_type,
                    activity_label: term_string(&solution, "activityLabel")?,
                    child_index: term_string(&solution, "childIndex")?,
                    child_model: term_string(&solution, "childModel")?,
                    precedes_target: term_string(&solution, "precedesTarget")?,
                    region_id: term_string(&solution, "regionId")?,
                    sparql_projection: term_string(&solution, "sparqlProjection")?,
                    tera_renderer: term_string(&solution, "teraRenderer")?,
                    derived_from: term_string(&solution, "derivedFrom")?,
                    start_node: term_string(&solution, "startNode")?,
                    end_node: term_string(&solution, "endNode")?,
                    routing_node: term_string(&solution, "routingNode")?,
                    routing_node_child_index: term_string(&solution, "routingNodeChildIndex")?,
                    edge_id: term_string(&solution, "edgeId")?,
                    edge_source: term_string(&solution, "edgeSource")?,
                    edge_target: term_string(&solution, "edgeTarget")?,
                    process_invariant: term_string(&solution, "processInvariant")?,
                    exogenous_event: term_string(&solution, "exogenousEvent")?,
                    resource_annotation: term_string(&solution, "resourceAnnotation")?,
                    guard_condition: term_string(&solution, "guardCondition")?,
                });
            }
        }
        _ => {
            return Err(Refusal::ValidationFailed(
                "render_model_projection.rq did not yield SELECT solutions".to_string(),
            ))
        }
    }
    Ok(rows)
}

/// Reads `var` out of `solution` as its lexical string form: an IRI's full
/// IRI text, or a literal's lexical value. Returns `Ok(None)` when `var` is
/// unbound (the common case for an `OPTIONAL` block that did not match this
/// row's element kind).
///
/// # Complexity
/// O(1).
fn term_string(solution: &QuerySolution, var: &str) -> Result<Option<String>, Refusal> {
    match solution.get(var) {
        None => Ok(None),
        Some(Term::NamedNode(n)) => Ok(Some(n.as_str().to_string())),
        Some(Term::Literal(l)) => Ok(Some(l.value().to_string())),
        Some(other) => Err(Refusal::ValidationFailed(format!(
            "projection variable ?{var} bound to unsupported term kind: {other}"
        ))),
    }
}

