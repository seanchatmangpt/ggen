/// True when `model` contains at least one [`Powl::ExternalCut`] node
/// anywhere in the tree (a `PartialOrder`/`Choice` container's children are
/// scanned, but an already-found cut's own `region` is not further
/// descended into -- the same traversal boundary [`admit_powl_model`] uses,
/// since a cut's region is opaque to the projecting workflow by definition,
/// PRD.md sec.7.4).
///
/// This is the trigger `ChatmanEngine::admit_transition_with_external_cut`
/// uses to decide whether the real Rail A/B pipeline runs at all for a
/// given admission: PRD.md sec.19.1's "local POWL remains local" scenario
/// means a region with no declared cut never engages the pipeline.
///
/// # Complexity
/// O(n) in the total node count of the model tree; short-circuits on the
/// first hit.
pub fn model_declares_external_cut(model: &Powl) -> bool {
    match model {
        Powl::Leaf(_) => false,
        Powl::PartialOrder { children, .. } | Powl::Choice { children, .. } => {
            children.iter().any(model_declares_external_cut)
        }
        Powl::ExternalCut { .. } => true,
    }
}

