/// on `praxis-core` (see this section's header comment), so it defines its
/// own copy of the shape instead of importing it.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct ExternalCutCompilationOutcome {
    /// BLAKE3 digest (hex) of `region_turtle`.
    pub source_powl_digest_hex: String,
    /// BLAKE3 digest (hex) of the SPARQL query text actually executed (Q).
    pub sparql_projection_digest_hex: String,
    /// BLAKE3 digest (hex) of the Tera template text actually rendered (T).
    pub tera_template_digest_hex: String,
    /// BLAKE3 digest (hex) of the manufactured Arazzo JSON document.
    pub arazzo_digest_hex: String,
    /// The compiler version string folded into the receipt.
    pub compiler_version: String,
    /// BLAKE3 digest (hex) of the compiled AIR module
    /// (`wasm4pm_arazzo::compile::AirCompiler::digest_program`).
    pub air_digest_hex: String,
    /// The manufactured Arazzo 1.1.x JSON document text (`A_z`).
    pub arazzo_document: String,
}

/// Rail A/B pipeline seam (PROJ-796). See this section's header comment for
/// why this is a trait rather than a direct call. Implementors must be
/// deterministic and perform no I/O beyond in-memory computation (no wall
/// clock, no randomness) -- the same discipline every other stage in this
/// module follows.
pub trait ExternalCutCompiler {
    /// Runs the real Q(W) SPARQL projection, T(...) Tera render, Arazzo
    /// parse/normalize, and AIR lowering/compilation over `request`.
    fn compile(
        &self,
        request: &ExternalCutCompilationRequest<'_>,
    ) -> Result<ExternalCutCompilationOutcome, Refusal>;
}

