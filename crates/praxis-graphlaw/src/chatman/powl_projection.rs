//! Projects Chatman Engine PDDL plan tapes ([`Pddl8Tape`]) into POWL 2.0
//! models ([`Powl`], Kourani et al. Defs 3.6-3.9), admits and serializes
//! those models as RDF Turtle conforming to the `powl2:` vocabulary
//! (`wasm4pm-compat/ontologies/powlv2.ttl`), and executes the Q-stage
//! SPARQL projection of PRD.md sec.7.4's `A_z = T(Q(W))` external-cut
//! pipeline against that Turtle ([`run_render_model_projection`]).
//!
//! The projection functions ([`project_pddl_tape_to_powl`],
//! [`project_pddl_tape_to_powl_hierarchical`], [`powl_to_turtle`]) are pure:
//! no I/O, no wall clock, no randomness. IRIs generated by [`powl_to_turtle`]
//! are derived deterministically from `base_iri` plus a stable structural
//! path (child index at each level of the tree), never from a blank-node
//! counter or hash map iteration order. [`run_render_model_projection`] is
//! the one exception to "no I/O": the query text it executes is embedded at
//! compile time (`include_str!`, not a runtime read), and the SPARQL
//! evaluation itself runs over an in-memory oxigraph store built solely from
//! its `turtle` argument -- no ambient state, no network, no wall clock.

