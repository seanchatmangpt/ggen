#[derive(Clone, Copy, Debug, PartialEq, Eq)]
struct 試験実行器 {
    実行回数: u32,
}

impl 実行器 for 試験実行器 {
    type 成果 = 道具番号;
    type 失敗 = ();

    fn 実行する(
        &mut self,
        許可: 許可済み選択,
        現在時刻: 時刻,
    ) -> 成否<(Self::成果, 受領証), Self::失敗> {
        self.実行回数 += 1;
        let 道具 = 許可.道具();
        let 提案 = 許可.提案();
        成否::成功((
            道具,
            受領証 {
                受領番号: 現在時刻,
                種別: 受領種別::実行,
                工程: 0,
                時刻: 現在時刻,
                標準番号: 提案.方策番号,
                異常: 有無::無い,
                道具: 有無::有る(道具),
                理由: 有無::無い,
                前要約: 許可.許可受領証().後要約,
                後要約: 提案.方策要約,
            },
        ))
    }
}

#[test]
fn 許可済み選択だけが実行器へ渡る() {
    let 方策 = 方策を作る();
    let 要求 = 選択要求 {
        取得権限: 0b001,
        準備完了: 0b001,
        最大時間: 999,
        決定性必須: true,
        受領証必須: true,
        時刻: 1000,
    };
    let mut 作業領域 = 作業領域::<3>::新規();
    let 提案 = match 選択する(&方策, 要求, &mut 作業領域) {
        選択結果::選択(提案) => 提案,
        選択結果::拒否 { .. } => panic!("選択に失敗した"),
    };
    let 仲介者 = 仲介者 {
        必要権限: 0b001,
        方策要約: 方策.方策要約,
    };
    let 許可 = match 仲介者.許可する(
        提案,
        能力札 { 許可道具: 1, 権限: 0b001, 有効期限: 2000 },
        1500,
    ) {
        成否::成功(許可) => 許可,
        成否::失敗(_) => panic!("許可に失敗した"),
    };
    let mut 実行器 = 試験実行器 { 実行回数: 0 };
    match 実行器.実行する(許可, 1600) {
        成否::成功((道具, 受領証)) => {
            assert_eq!(道具, 1);
            assert_eq!(実行器.実行回数, 1);
            assert_eq!(受領証.種別, 受領種別::実行);
        }
        成否::失敗(_) => panic!("実行に失敗した"),
    }
}


