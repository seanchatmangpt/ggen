// 改善と再開

#[test]
fn 異常後は改善と標準更新なしに再開できない() {
    let 生産線 = 生産線::<稼働中>::新設(1, 100);
    let 停止線 = match 生産線.作業する(品質判定::異常(異常::標準逸脱), 20) {
        作業結果::異常停止 { 生産線, .. } => 生産線,
        作業結果::良品完成 { .. } => panic!("停止しなかった"),
    };

    let 異常票 = 異常票 {
        工程: 1,
        異常: 異常::標準逸脱,
        発生時刻: 20,
        発生数量: 0,
    };

    let 改善票 = 改善票 {
        改善番号: 1,
        元異常: 異常票,
        真因: 真因::標準不足,
        対策: 対策::標準変更,
        旧標準: 100,
        新標準: 101,
    };

    let 対策工程 = match 停止線.真因対策を登録する(改善票) {
        成否::成功(工程) => 工程,
        成否::失敗(_) => panic!("真因対策を登録できなかった"),
    };
    let 再開工程 = 対策工程.対策を完了する();

    let mut 旧標準 = 標準作業::<4>::新規(100, 有無::無い, 1, 60, 1);
    let 新標準 = 標準作業::<4>::新規(101, 有無::有る(100), 1, 55, 1);

    let 再開線 = match 再開工程.標準を更新して再開する(&mut 旧標準, &新標準) {
        成否::成功(線) => 線,
        成否::失敗(_) => panic!("標準更新後に再開できなかった"),
    };

    assert_eq!(再開線.標準番号(), 101);
    assert!(!旧標準.現行か());
}

