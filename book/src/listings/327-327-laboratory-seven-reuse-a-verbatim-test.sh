#!/usr/bin/env bash
set -euo pipefail

subject="327. Laboratory Seven: Reuse a Verbatim Test"
before="$(mktemp -d)"
after="$(mktemp -d)"

ggen sync run
cp -R src "$before/"
cargo test --all-targets
ggen sync run
cp -R src "$after/"

diff -ru "$before/src" "$after/src"
printf 'verified: %s\n' "$subject"
